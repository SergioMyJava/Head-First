package chapter6.device;

public class GarageDoor {

    public void up(){
        System.out.println("Garage door open!");
    }

    public void down(){
        System.out.println("Garage door closed!");
    }

    public void stop(){
        System.out.println("Garage door stop!");
    }

    public void lightOn(){
        System.out.println("Garage door lightOn!");
    }

    public void lightOff(){
        System.out.println("Garage door lightOff!");
    }
}
